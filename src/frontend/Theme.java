package frontend;
import org.eclipse.swt.graphics.Color;
import org.eclipse.swt.widgets.Display;


public class Theme {
	private Color white;
	private Color black;
	private Color orange;
	private Color grey;
	private Color background_light;
	private Color background_dark;








	public Theme() {
		white = new Color(Display.getCurrent(), 255, 255, 255);
		grey = new Color(Display.getCurrent(), 200, 200, 200);
		black = new Color(Display.getCurrent(), 0, 0, 0);
		orange = new Color(Display.getCurrent(), 255, 102, 200);
		background_light = new Color(Display.getCurrent(), 230, 230, 230);
		background_dark = (new Color(Display.getCurrent(), 60, 60, 60));
	}




	public Color getWhite() {
		return white;
	}




	public Color getBlack() {
		return black;
	}




	public Color getOrange() {
		return orange;
	}




	public Color getGrey() {
		return grey;
	}


	public Color getBackground_light() {
		return background_light;
	}


	public Color getBackground_dark() {
		return background_dark;
	}


}

